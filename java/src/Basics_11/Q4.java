package Basics_11;
//  Write a Java program to print the result of the following operations.
public class Q4 {
    public static void main(String[] args) {
        int eq1 =  -5 + 8 * 6;
        int eq2 = (55+9) % 9;
        int eq3 = 20 + -3*5 / 8;
        int eq4 = 5 + 15 / 3 * 2 - 8 % 3;
        System.out.println("The answer of 1st equation is : "+eq1);
        System.out.println("The answer of 2nd equation is : "+eq2);
        System.out.println("The answer of 3rd equation is : "+eq3);
        System.out.println("The answer of 4th equation is : "+eq4);
    }
}
